
## Linked Lists - Challenge 3 Remove Duplicates ## 


class Node:
    def __init__(self, value):
        self.value = value
        self.next = None
        
class LinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.length = 1
        
    def print_list(self):
        temp = self.head
        while temp is not None:
            print(temp.value)
            temp = temp.next  
            
    def print_all(self):
        if self.length == 0:
            print("Head: None")
        else:
            print("Head: ", self.head.value)
        print("Length: ", self.length)
        print("\nLinked List:")
        if self.length == 0:
            print("empty")
        else:
            self.print_list()

    def append(self, value):
        new_node = Node(value)
        if self.length == 0:
            self.head = new_node
        else:
            current = self.head
            while current.next is not None:
                current = current.next
            current.next = new_node
        self.length += 1
    
    def remove_duplicates(self):
        values = ()
        prev = None 
        current = self.head
        if self.length == 0:
            return False
        while current is not None:
            if current.value in values:
                prev.next = current.next
                self.length -= 1
            else:
                values += (current.value,)
                prev = current
            current = current.next
    
        



